def build_dre(ca_jobs: list[dict], mp_expenses: list[dict], extrato_summary: dict,
              payments_ref: dict[str, dict]):
    """Build and print the DRE."""

    # ── Aggregate CA jobs by category ──
    receitas_by_cat = defaultdict(lambda: {"count": 0, "total": D("0")})
    despesas_by_cat = defaultdict(lambda: {"count": 0, "total": D("0")})

    for job in ca_jobs:
        cat = job["ca_category"]
        val = D(str(job["valor"]))
        if job["direction"] == "receita":
            receitas_by_cat[cat]["count"] += 1
            receitas_by_cat[cat]["total"] += val
        else:
            despesas_by_cat[cat]["count"] += 1
            despesas_by_cat[cat]["total"] += val

    # ── Aggregate mp_expenses (pending manual import) ──
    expenses_by_type = defaultdict(lambda: {"count": 0, "total": D("0")})
    expenses_income_by_type = defaultdict(lambda: {"count": 0, "total": D("0")})
    for exp in mp_expenses:
        etype = exp.get("expense_type") or "unknown"
        val = D(str(exp.get("amount", 0)))
        direction = exp.get("expense_direction", "expense")
        if direction == "income":
            expenses_income_by_type[etype]["count"] += 1
            expenses_income_by_type[etype]["total"] += val
        else:
            expenses_by_type[etype]["count"] += 1
            expenses_by_type[etype]["total"] += val

    # ── Print DRE ──
    W = 80

    print()
    print("=" * W)
    print("  DRE - DEMONSTRAÇÃO DO RESULTADO DO EXERCÍCIO")
    print("  141air - Janeiro 2026 (por COMPETÊNCIA / date_approved)")
    print("  (Simulação com lógica CORRIGIDA do processor)")
