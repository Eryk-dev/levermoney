def extract_charges(raw_payment: dict) -> dict:
    """Extract charges using FIXED logic (includes coupon_fee)."""
    amount = _to_float(raw_payment.get("transaction_amount"))
    net = _to_float((raw_payment.get("transaction_details") or {}).get("net_received_amount"))
    charges = raw_payment.get("charges_details") or []

    shipping_charges_collector = 0.0
    mp_fee = 0.0
    coupon_fee = 0.0

    for charge in charges:
        accounts = charge.get("accounts") or {}
        if accounts.get("from") != "collector":
            continue

        amounts = charge.get("amounts") or {}
        charge_amount = _to_float(amounts.get("original"))
        charge_type = (charge.get("type") or "").lower()
        charge_name = (charge.get("name") or "").strip().lower()

        if charge_type == "shipping":
            shipping_charges_collector += charge_amount
        elif charge_type == "fee":
            if charge_name == "financing_fee":
                continue
            mp_fee += charge_amount
        elif charge_type == "coupon":
            coupon_fee += charge_amount
            mp_fee += charge_amount  # coupon is lumped into comissao

    shipping_amount_buyer = _to_float(raw_payment.get("shipping_amount"))
    shipping_cost_seller = round(max(0.0, shipping_charges_collector - shipping_amount_buyer), 2)
    mp_fee = round(mp_fee, 2)

    reconciled_net = round(amount - mp_fee - shipping_cost_seller, 2)
    net_diff = round(net - reconciled_net, 2)
    subsidy = round(net - reconciled_net, 2) if net_diff > 0 else 0.0

    return {
        "amount": amount,
        "mp_fee": mp_fee,
        "coupon_fee": round(coupon_fee, 2),
        "shipping_cost_seller": shipping_cost_seller,
        "net": net,
        "reconciled_net": reconciled_net,
        "subsidy": max(0, subsidy),
    }


def extract_refund_charges(raw_payment: dict) -> dict:
    """Extract refund-specific charges using FIXED logic."""
    charges = raw_payment.get("charges_details") or []
    refunded_fee = 0.0
    refunded_shipping = 0.0

    for charge in charges:
        accounts = charge.get("accounts") or {}
        if accounts.get("from") != "collector":
            continue
        charge_type = str(charge.get("type") or "").lower()
        charge_name = str(charge.get("name") or "").strip().lower()
        if charge_name == "financing_fee":
            continue
        refunded_val = _to_float((charge.get("amounts") or {}).get("refunded"))
        if charge_type == "fee":
            refunded_fee += refunded_val
        elif charge_type == "shipping":
            refunded_shipping += refunded_val

    return {
        "refunded_fee": round(refunded_fee, 2),
        "refunded_shipping": round(refunded_shipping, 2),
    }


def simulate_ca_jobs(payments: dict[str, dict]) -> list[dict]:
    """Simulate all CA jobs that the FIXED processor would create for January payments."""
    jobs = []

    for pid, row in payments.items():
        raw = row.get("raw_payment")
        if not raw:
            continue

        ml_status = row.get("ml_status", "")
        status_detail = (raw.get("status_detail") or "")
        amount = _to_float(raw.get("transaction_amount"))

        # Determine processing path
        is_approved = ml_status in ("approved", "in_mediation")
        is_reimbursed_chargeback = (ml_status == "charged_back" and status_detail == "reimbursed")
        is_refunded = ml_status in ("refunded", "charged_back") and not is_reimbursed_chargeback
        is_refunded_by_admin = (ml_status == "refunded" and status_detail == "by_admin")

        if not (is_approved or is_reimbursed_chargeback or is_refunded):
            continue

        charges = extract_charges(raw)

        # For refunded_by_admin that was never synced in a "live" scenario during backfill:
        # the processor would skip it (not create receita). But in a fresh backfill,
        # refunded payments that have a split payment already get skipped.
        # For simplicity, we follow the same logic as processor:
        # 1) Create receita + despesas (approved path)
        # 2) If refunded: also create estornos

        # Step 1: Always create receita + despesas (processor creates them first, even for refunds)
        jobs.append({
            "payment_id": pid,
            "job_type": "receita",
            "ca_category": "venda_ml",
            "valor": charges["amount"],
            "direction": "receita",  # contas-a-receber
        })

        if charges["mp_fee"] > 0:
            jobs.append({
                "payment_id": pid,
                "job_type": "comissao",
                "ca_category": "comissao_ml",
                "valor": charges["mp_fee"],
                "direction": "despesa",  # contas-a-pagar
            })

        if charges["shipping_cost_seller"] > 0:
            jobs.append({
                "payment_id": pid,
                "job_type": "frete",
                "ca_category": "frete_mercadoenvios",
                "valor": charges["shipping_cost_seller"],
                "direction": "despesa",
            })

        if charges["subsidy"] >= 0.01:
            jobs.append({
                "payment_id": pid,
                "job_type": "subsidio",
                "ca_category": "estorno_frete",
                "valor": charges["subsidy"],
                "direction": "receita",
            })

        # Step 2: For refunded payments, create estornos
        if is_refunded:
            refunds = raw.get("refunds") or []
            total_refunded_raw = sum(r.get("amount", 0) for r in refunds) if refunds else amount
            estorno_receita = min(total_refunded_raw, amount)

            jobs.append({
                "payment_id": pid,
                "job_type": "estorno",
                "ca_category": "devolucao",
                "valor": estorno_receita,
                "direction": "despesa",  # contas-a-pagar (devolve dinheiro)
            })

            refund_charges = extract_refund_charges(raw)

            if refund_charges["refunded_fee"] > 0 and estorno_receita >= amount:
                jobs.append({
                    "payment_id": pid,
                    "job_type": "estorno_taxa",
                    "ca_category": "estorno_taxa",
                    "valor": refund_charges["refunded_fee"],
                    "direction": "receita",  # contas-a-receber (ML devolve taxa)
                })

            if refund_charges["refunded_shipping"] > 0 and estorno_receita >= amount:
                jobs.append({
                    "payment_id": pid,
                    "job_type": "estorno_frete",
                    "ca_category": "estorno_frete",
                    "valor": refund_charges["refunded_shipping"],
                    "direction": "receita",  # contas-a-receber (ML devolve frete)
                })

    return jobs


