    all_payments = []
    offset = 0
    batch_size = 500
    skipped_statuses = Counter()

    # Load ALL payments for seller (paginated, raw_payment needed for date_approved)
    while True:
        result = (
            db.table("payments")
            .select("ml_payment_id, amount, net_amount, ml_status, status, "
                    "money_release_date, processor_fee, processor_shipping, raw_payment")
            .eq("seller_slug", SELLER_SLUG)
            .range(offset, offset + batch_size - 1)
            .execute()
        )
